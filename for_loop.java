import java.util.Scanner;
public class for_loop {
  public static void main(String[] args) {
    Scanner sc = new Scanner(System.in);
    System.out.println("Enter the number: ");
    int a=sc.nextInt();
    sc.close();
    
    int x;
    for(x=0;x<=a;x++){
        System.out.println((2*x+1));
    }
    
    }
    }
    

