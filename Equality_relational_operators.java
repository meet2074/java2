public class Equality_relational_operators {
	public static void main(String[] args) {
		int value1=1;
		int value2=2;
		 if (value1==value2)
			 System.out.println("Value1=value2");
		 if (value1!=value2)
			 System.out.println("Value1!=value2");
		 
		 if (value1<value2)
			 System.out.println("Value1<value2");
		 if (value1>value2)
			 System.out.println("Value1>value2");	 
	}

}
